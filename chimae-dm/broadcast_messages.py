def get_message(week: int, day: str, cname: str = "") -> str:
    """week: 1~8, day: '화' or '금', cname: 고객 이름."""
    if week not in WEEKS or day not in WEEKS[week]:
        return ""
    tmpl = WEEKS[week][day]["text"]
    return tmpl.replace("{cname}", cname) if cname else tmpl


def get_topic(week: int, day: str) -> str:
    if week not in WEEKS or day not in WEEKS[week]:
        return ""
    return WEEKS[week][day]["topic"]


def is_ad(week: int, day: str) -> bool:
    if week not in WEEKS or day not in WEEKS[week]:
        return False
    return WEEKS[week][day]["is_ad"]


