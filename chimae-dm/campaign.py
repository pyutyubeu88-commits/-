def render_message(item: dict) -> str:
    """멘트의 {name}{title}{tel} 치환."""
    return item["message"].format(
        name="○○", title=AGENT["title"], name_=AGENT["name"], tel=AGENT["tel"],
    ) if False else item["message"].replace("{name}", "○○") \
        .replace("{title}", AGENT["title"]) \
        .replace("{tel}", AGENT["tel"]) \
        .replace("{name}", AGENT["name"])


def card_plan() -> dict:
    """카드 유형별 집계 — 데이터 카드 vs 관계·상담 카드."""
    plan = {DATA: [], RELATE: [], TEXT: []}
    for it in CAMPAIGN:
        plan[it["card"]].append(it["day"])
    return plan


